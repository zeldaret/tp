J3DAnmTransform* daNpcCd_c::getAnmP(int param_1, int param_2) {
    int a_anmNum = param_1;
    switch (a_anmNum) {
    case 0:
    case 4:
    case 5:
    case 6:
        switch (param_2) {
        case 1:
        case 8:
            a_anmNum = 6;
            break;

        case 2:
        case 3:
            a_anmNum = 5;
            break;

        case 4:
        case 5:
            a_anmNum = 4;
        }
        break;

    case 1:
        switch (param_2) {
        case 1:
        case 8:
            a_anmNum = 6;
            break;

        case 2:
        case 3:
            a_anmNum = 5;
            break;

        case 4:
        case 5:
            a_anmNum = 4;
        }
        break;

    case 2:
    case 3:
        switch (param_2) {
        case 1:
        case 8:
            a_anmNum = 9;
            break;

        case 2:
        case 3:
            a_anmNum = 8;
            break;

        case 4:
        case 5:
            a_anmNum = 7;
        }
        break;

    default:
        return NULL;
    }

    anmTblPrm anmTbl;
    if ((isM_() & 0xFF) != 0) {
        anmTbl = l_bckTbl_M[a_anmNum];
    } else {
        anmTbl = l_bckTbl_W[a_anmNum];
    }

    return (J3DAnmTransform*)dComIfG_getObjectRes(anmTbl.arc_name, anmTbl.resource_index);
}

